void	arg_to_array(char input[4][4], char *str)
{
	int	x;
	int	y;

	y = 0;
	while (y < 4)
	{	
		x = 0;
		while (x < 4)
		{
			input[y][x] = *str;
			str++;
			str++;
			x++;
		}
		y++;
	}
}
