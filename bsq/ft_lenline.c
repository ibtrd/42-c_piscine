#include "ft.h"

int	ft_lenline(char *raw_map)
{
	int	i;
	int	n;

	while (raw_map[i] != '\n')
		i++;
	while (raw_map[i] != '\n')
	{
		i++;
		n++;
	}
	return (n);
}
