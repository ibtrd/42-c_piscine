#include <unistd.h>

char	*ft_strncat(char *dest, char *src, unsigned int nb);

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(void)
{
	unsigned int	nb;
	char	dest[100] = {"Coucou"};
	char	src[100] = {"MonBichon"};

	nb = 3;

	ft_putstr(dest);
	ft_putchar('\n');
	ft_putstr(src);
	ft_putchar('\n');
	ft_putstr(ft_strncat(dest, src, nb));
	ft_putchar('\n');
	return (0);
}
