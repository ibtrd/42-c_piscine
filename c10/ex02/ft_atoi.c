int	ft_atoi(char *str)
{
	int	nb;
	int	is_neg;
	int	i;

	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	is_neg = 0;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			is_neg++;
		i++;
	}
	nb = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	if (is_neg % 2 != 0)
		nb *= -1;
	return (nb);
}
